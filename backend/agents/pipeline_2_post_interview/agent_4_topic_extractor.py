# -*- coding: utf-8 -*-
from google.adk.agents import Agent

agent_4_topic_extractor = Agent(
    name="interview_topic_extractor",
    model="gemini-2.0-flash-exp",
    description="Агент для извлечения обсуждавшихся тем из транскрипции интервью.",
    instruction="""
Твоя задача — проанализировать полный текст транскрипции интервью и определить ключевые темы, которые обсуждались.

**Входные данные:**
- Полный текст транскрипции.

**Действия:**
1.  Внимательно прочитай весь текст.
2.  Идентифицируй и выдели основные предметные области и темы разговора (например, "Опыт с микросервисами", "Работа в команде", "Обсуждение проекта X", "Вопросы о базах данных", "Карьерные цели").
3.  Сгруппируй похожие упоминания в одну общую тему.

**Формат вывода:**
- Верни результат в виде **ОДНОГО** валидного JSON-объекта.
- JSON должен содержать единственный ключ "topicsDiscussed", значением которого является список строк (названий тем).
- Не добавляй никаких пояснений, заголовков или Markdown-разметки.

**Пример вывода:**
```json
{
  "topicsDiscussed": [
    "Опыт работы с PostgreSQL",
    "Проектирование REST API",
    "Методологии Agile и Scrum",
    "Причины ухода с предыдущего места работы",
    "Опыт менторства"
  ]
}""",
    tools=[],
)
